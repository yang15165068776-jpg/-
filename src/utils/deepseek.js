function buildUserWrapper(character, affections) {
  let stageCalibration = ''

  if (character?.chatStyle === 'story' && character?.romanceCharacters) {
    const calibrations = character.romanceCharacters
      .filter(rc => rc.affectionEnabled)
      .map(rc => {
        const affValue = affections?.[rc.name] ?? rc.affectionInitial ?? 50
        const stage = getCurrentAffectionStage(rc, affValue)
        if (!stage) return null
        const lines = [
          rc.name + 'пҪң' + stage.name + 'пҪңеҘҪж„ҹеәҰ' + affValue,
        ]
        if (stage.forbiddenBehaviors) lines.push('зҰҒжӯўпјҡ' + stage.forbiddenBehaviors)
        if (stage.languageSamples) lines.push('иҜӯиЁҖж ·жң¬пјҡ' + stage.languageSamples)
        if (stage.playerStrategy) lines.push('зӯ–з•Ҙпјҡ' + stage.playerStrategy)
        return lines.join('\n')
      })
      .filter(Boolean)

    if (calibrations.length > 0) {
      stageCalibration = 'гҖҗйҳ¶ж®өж ЎеҮҶгҖ‘\n' + calibrations.join('\n\n') + '\n\n'
    }
  }

  return `

---
${stageCalibration}гҖҗжң¬иҪ®дёүжқЎеә•зәҝпјҢиҝқеҸҚд»»дҪ•дёҖжқЎз«ӢеҚійҮҚеҶҷгҖ‘
1. и§’иүІиҜӯж°”дёҚиғҪжҜ”иҜӯиЁҖж ·жң¬жӣҙжё©жҹ”
2. з»“е°ҫдёҚиғҪи®©еңәйқўе№іжҒҜжҲ–и®©зҺ©е®¶ж„ҹеҲ°иў«е®үж…°
3. жҜҸ300еӯ—иҮіе°‘дёҖеӨ„еҝғзҗҶеұӮ

з”ҹжҲҗиҚүзЁҝеҚіеҸҜпјҢеҗҺз»ӯжңүдё“йЎ№дјҳеҢ–гҖӮ`
}

